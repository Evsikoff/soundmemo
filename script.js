const board = document.getElementById("board");
const levelTitle = document.getElementById("levelTitle");
const levelDescription = document.getElementById("levelDescription");
const levelProgress = document.getElementById("levelProgress");
const moveCounter = document.getElementById("moveCounter");
const pairCounter = document.getElementById("pairCounter");
const hintButton = document.getElementById("hintButton");
const menuButton = document.getElementById("menuButton");
const adModal = document.getElementById("adModal");
const levelCompleteModal = document.getElementById("levelCompleteModal");
const completeTitle = document.getElementById("completeTitle");
const completeSummary = document.getElementById("completeSummary");
const starRow = document.getElementById("starRow");
const restartLevel = document.getElementById("restartLevel");
const nextLevel = document.getElementById("nextLevel");
const summaryModal = document.getElementById("summaryModal");
const summaryList = document.getElementById("summaryList");
const closeSummary = document.getElementById("closeSummary");
const appEl = document.querySelector(".app");

let currentLevelIndex = 0;
let currentCards = [];
let currentAudio = null;
let lastSound = null;
let lastCardIndex = null;
let moves = 0;
let pairsFound = 0;
let hintsUsed = 0;
const starsByLevel = new Array(levelData.length).fill(0);

let ysdk = null;
let player = null;

const shuffle = (array) => {
  const cloned = [...array];
  for (let i = cloned.length - 1; i > 0; i -= 1) {
    const j = Math.floor(Math.random() * (i + 1));
    [cloned[i], cloned[j]] = [cloned[j], cloned[i]];
  }
  return cloned;
};

const wait = (ms) => new Promise((resolve) => setTimeout(resolve, ms));

const applyTheme = (theme) => {
  document.documentElement.style.setProperty("--bg", theme.bg);
  document.documentElement.style.setProperty("--accent", theme.accent);
  document.documentElement.style.setProperty("--accent-strong", theme.strong);
};

const buildCards = (level) => {
  const sounds = shuffle(level.sfx).slice(0, 8);
  const paired = shuffle([...sounds, ...sounds]);
  currentCards = paired.map((sound, index) => ({
    id: index,
    sound,
    removed: false,
  }));
};

const renderBoard = (level) => {
  board.innerHTML = "";
  currentCards.forEach((card, index) => {
    const cardEl = document.createElement("button");
    cardEl.className = "card";
    cardEl.style.setProperty("--card-cover", `url(${level.cover})`);
    cardEl.dataset.index = index;

    const label = document.createElement("span");
    label.className = "card__label";
    label.textContent = "Звук";
    cardEl.appendChild(label);

    cardEl.addEventListener("click", () => handleCardClick(index));
    board.appendChild(cardEl);
  });
};

const updateMeta = () => {
  levelProgress.textContent = `${currentLevelIndex + 1} / ${levelData.length}`;
  moveCounter.textContent = moves;
  pairCounter.textContent = `${pairsFound} / 8`;
};

const stopCurrentAudio = () => {
  if (currentAudio) {
    currentAudio.pause();
    currentAudio.currentTime = 0;
  }
};

const playSound = (sound) => {
  stopCurrentAudio();
  currentAudio = new Audio(sound);
  currentAudio.play();
};

const highlightCard = (index) => {
  board.querySelectorAll(".card").forEach((cardEl) => {
    cardEl.classList.toggle("active", Number(cardEl.dataset.index) === index);
  });
};

const removePair = (firstIndex, secondIndex) => {
  stopCurrentAudio();
  highlightCard(null);
  lastSound = null;
  lastCardIndex = null;
  [firstIndex, secondIndex].forEach((idx) => {
    currentCards[idx].removed = true;
    const cardEl = board.querySelector(`.card[data-index='${idx}']`);
    if (cardEl) {
      cardEl.classList.add("removed");
    }
  });
  pairsFound += 1;
  updateMeta();
};

const handleCardClick = (index) => {
  const card = currentCards[index];
  if (!card || card.removed) {
    return;
  }
  moves += 1;
  updateMeta();
  highlightCard(index);
  playSound(card.sound);

  if (lastSound && lastSound === card.sound && lastCardIndex !== index) {
    removePair(lastCardIndex, index);
    lastSound = null;
    lastCardIndex = null;
    if (pairsFound === 8) {
      finishLevel();
    }
    return;
  }

  lastSound = card.sound;
  lastCardIndex = index;
};

const showAd = async (title) => {
  adModal.classList.remove("hidden");
  document.getElementById("adTitle").textContent = title;
  await wait(2000);
  adModal.classList.add("hidden");
};

const calculateStars = () => {
  let stars = 5;
  const movePenalty = Math.max(0, Math.floor((moves - 12) / 6));
  stars -= movePenalty;
  stars -= hintsUsed;
  return Math.max(1, Math.min(5, stars));
};

const renderStars = (count) => {
  starRow.innerHTML = "";
  for (let i = 1; i <= 5; i += 1) {
    const star = document.createElement("span");
    star.className = "star";
    star.textContent = "★";
    if (i <= count) {
      star.classList.add("active");
    }
    starRow.appendChild(star);
  }
};

/* ── Progress persistence ────────────────────────────── */

const saveProgress = async () => {
  const data = { starsByLevel: [...starsByLevel], currentLevelIndex };
  try {
    localStorage.setItem("soundmemo_progress", JSON.stringify(data));
  } catch (e) { /* ignore */ }
  try {
    if (player) await player.setData(data, true);
  } catch (e) { /* ignore */ }
};

const loadProgress = async () => {
  // Cloud saves first
  try {
    if (player) {
      const data = await player.getData(["starsByLevel", "currentLevelIndex"]);
      if (data && Array.isArray(data.starsByLevel) && data.starsByLevel.length) {
        data.starsByLevel.forEach((s, i) => {
          if (i < starsByLevel.length) starsByLevel[i] = s;
        });
        if (typeof data.currentLevelIndex === "number") {
          currentLevelIndex = data.currentLevelIndex;
        }
        return;
      }
    }
  } catch (e) { /* ignore */ }
  // Fallback to localStorage
  try {
    const raw = localStorage.getItem("soundmemo_progress");
    if (raw) {
      const data = JSON.parse(raw);
      if (Array.isArray(data.starsByLevel)) {
        data.starsByLevel.forEach((s, i) => {
          if (i < starsByLevel.length) starsByLevel[i] = s;
        });
      }
      if (typeof data.currentLevelIndex === "number") {
        currentLevelIndex = data.currentLevelIndex;
      }
    }
  } catch (e) { /* ignore */ }
};

/* ── Level lifecycle ─────────────────────────────────── */

const finishLevel = async () => {
  stopCurrentAudio();
  const stars = calculateStars();
  starsByLevel[currentLevelIndex] = Math.max(starsByLevel[currentLevelIndex], stars);
  await saveProgress();
  completeTitle.textContent = `Вы нашли все пары!`;
  renderStars(stars);
  completeSummary.textContent = `Ходы: ${moves}. Подсказки: ${hintsUsed}.`;
  levelCompleteModal.classList.remove("hidden");
  ysdk?.features?.GameplayAPI?.stop();
};

const resetLevelState = () => {
  moves = 0;
  pairsFound = 0;
  hintsUsed = 0;
  lastSound = null;
  lastCardIndex = null;
  updateMeta();
};

const loadLevel = (index) => {
  currentLevelIndex = index;
  const level = levelData[index];
  applyTheme(level.theme);
  levelTitle.textContent = level.title;
  levelDescription.textContent = level.description;
  resetLevelState();
  buildCards(level);
  renderBoard(level);
  updateMeta();
};

const removeRandomPair = () => {
  const available = currentCards
    .map((card, index) => ({ ...card, index }))
    .filter((card) => !card.removed);
  if (available.length < 2) {
    return;
  }
  const mapBySound = available.reduce((acc, card) => {
    acc[card.sound] = acc[card.sound] || [];
    acc[card.sound].push(card.index);
    return acc;
  }, {});
  const sounds = Object.keys(mapBySound).filter((sound) => mapBySound[sound].length >= 2);
  if (!sounds.length) {
    return;
  }
  const sound = sounds[Math.floor(Math.random() * sounds.length)];
  const [first, second] = mapBySound[sound];
  removePair(first, second);
  if (pairsFound === 8) {
    finishLevel();
  }
};

/* ── Summary / level menu ────────────────────────────── */

const renderSummary = () => {
  summaryList.innerHTML = "";
  levelData.forEach((level, index) => {
    const row = document.createElement("div");
    row.className = "summary-item";

    const info = document.createElement("div");
    info.innerHTML = `<strong>${level.title}</strong><div class="eyebrow">Уровень ${index + 1}</div>`;

    const stars = document.createElement("div");
    stars.className = "summary-stars";
    for (let i = 1; i <= 5; i += 1) {
      const dot = document.createElement("span");
      if (i <= starsByLevel[index]) {
        dot.classList.add("active");
      }
      stars.appendChild(dot);
    }

    const action = document.createElement("button");
    action.className = "ghost";
    action.textContent = "Играть";
    action.addEventListener("click", () => {
      summaryModal.classList.add("hidden");
      loadLevel(index);
      ysdk?.features?.GameplayAPI?.start();
    });

    row.appendChild(info);
    row.appendChild(stars);
    row.appendChild(action);
    summaryList.appendChild(row);
  });
};

/* ── Event listeners ─────────────────────────────────── */

hintButton.addEventListener("click", async () => {
  await showAd("Видео-реклама перед подсказкой");
  hintsUsed += 1;
  removeRandomPair();
});

menuButton.addEventListener("click", () => {
  ysdk?.features?.GameplayAPI?.stop();
  renderSummary();
  summaryModal.classList.remove("hidden");
});

restartLevel.addEventListener("click", async () => {
  levelCompleteModal.classList.add("hidden");
  await showAd("Полноэкранная реклама");
  loadLevel(currentLevelIndex);
  ysdk?.features?.GameplayAPI?.start();
});

nextLevel.addEventListener("click", async () => {
  levelCompleteModal.classList.add("hidden");
  await showAd("Полноэкранная реклама");
  if (currentLevelIndex < levelData.length - 1) {
    loadLevel(currentLevelIndex + 1);
    ysdk?.features?.GameplayAPI?.start();
    return;
  }
  ysdk?.features?.GameplayAPI?.stop();
  renderSummary();
  summaryModal.classList.remove("hidden");
});

closeSummary.addEventListener("click", () => {
  summaryModal.classList.add("hidden");
  ysdk?.features?.GameplayAPI?.start();
});

/* ── App initialization ──────────────────────────────── */

(async () => {
  appEl.classList.add("app--loading");

  // Initialize Yandex SDK
  try {
    ysdk = await YaGames.init();
  } catch (e) {
    console.warn("Yandex SDK init failed:", e);
  }

  // Initialize player
  try {
    if (ysdk) {
      player = await ysdk.getPlayer();
    }
  } catch (e) {
    console.warn("Player init failed:", e);
  }

  // Load saved progress (cloud first, then localStorage)
  await loadProgress();

  // Load level from saved progress
  loadLevel(currentLevelIndex);

  // SDK lifecycle signals
  try {
    ysdk?.features?.LoadingAPI?.ready();
    ysdk?.features?.GameplayAPI?.start();
  } catch (e) { /* ignore */ }

  const lang = ysdk?.environment?.i18n?.lang || "ru";
  document.documentElement.lang = lang;

  // Unblock UI
  appEl.classList.remove("app--loading");
})();
